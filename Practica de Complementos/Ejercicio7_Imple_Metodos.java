package Practicas_FullStack;

/*
Realm of Dragonfly era un proyecto de Juego que tenía pensado como proyecto.
Algo que recuerdo, era que existía un item especial cuyo poder base es P. Este item es
especial, ya que podía potenciar su poder en X puntos. El item puede potenciarse hasta un
máximo de N veces.
Calcule, cual es valor máximo que poder que puede tener el item.
 */
public class Ejercicio7_Imple_Metodos{
    
    public int sampleMethod(int P, int X, int N){

        return X*N+P;
    }
}
